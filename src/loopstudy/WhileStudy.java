package loopstudy;

public class WhileStudy {

	public static void main(String[] args) 
	{
		//while program
		int i=5;
		while(i<=50)
		{
			System.out.println(i);
			i=i+5;
		}
		
		//Do while program
		int j=5;
		do
		{
			System.out.println(j);
			j=j+5;
		}
		while(j<=50);

	}

}

package castingStudy;

public class PrimitiveCast 
{
	public static void main(String[] arg) 
	{
				
		//explicit casting
		int a=4;
		double b = a;
		System.out.println(b);
		System.out.println("==============");
		
		//Implicit casting
		double x=4.5;
		int y =(int) x;
		System.out.println(y);
		System.out.println("==============");
		
	}
}
